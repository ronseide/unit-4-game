$(document).ready (function() {
var isPlaying;
var target = 0;
var crystal = 0;
var gayle = 0;
var kristol = 0;
var roper = 0;
var tally = 0;
var computerScore = 0;
var userScore = 0;
// This function resets the game, generates the target number, displays it to the "target" div, resets var tally to 0, clears "tally" and "status" and calls function randoForButton.
$("#start").on("click", function(){
    isPlaying = true;
    target = Math.floor(Math.random() * 100) + 20;
    $("#target").html("The target number is " + target);
    tally = 0;
    $("#tally").html("");
    $("#status").html("");
    randoForButton ();
});
// This function is called in the previous function and assigns a random number to each of the button variables.
function randoForButton() {
    crystal = Math.floor(Math.random() * 12) + 1;
    gayle = Math.floor(Math.random() * 12) + 1;
    kristol = Math.floor(Math.random() * 12) + 1;
    roper = Math.floor(Math.random() * 12) + 1;
};
// Provided that the game has not been won or lost, these event handlers each increment var tally by the (random) value of the variable associated with the button element clicked. They then call the function update.
    $("#crystal").on("click", function() {
        if (isPlaying === true) {
            tally += crystal;
            update();
        }
    });
    $("#gayle").on("click", function() {
        if (isPlaying === true) {
        tally += gayle;
        update ();
        }
    });
    $("#kristol").on("click", function() {
        if (isPlaying === true) {
        tally += kristol;
        update ();
        }
    });
    $("#roper").on("click", function() {
        if (isPlaying === true) {
        tally += roper;
        update ();
        }
    });
// This function updates var computerScore and var userScore as and, if the game is won or lost, it displays the appropriate message to div "status". In the event of a win or a loss, it calls function display and disables the image buttons by changing var isPlaying to false which is reset to true with a click of button "Start Game".
function update () {
    $("#tally").html("Your tally is " + tally);
    if (tally > target) {
        $("#status").html("You Lose! Click Start Game to play again.");
        computerScore += 1;
        display ();
        isPlaying = false;
    }
    else
    if (tally === target) {
        $("#status").html("You Win! Click Start Game to play again.");
        userScore += 1;
        display ();
        isPlaying = false;
    }
};
// This function displays var computerScore and var userScore to the appropriate divs if a win or a loss is generated by function update. 
function display () {
    $("#computerScore").html("The computer's score is " + computerScore);
    $("#userScore").html("Your score is " + userScore);
};
});